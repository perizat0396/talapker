// ── Найти сотрудника по маркеру / предмету ────────────────────────────────────
function getStaffBySubject(subjectKey) {
  return STAFF_CONFIG.find(s => s.subjects.includes(subjectKey)) || null;
}

// ── Найти сотрудника по PIN ────────────────────────────────────────────────────
function getStaffByPin(pin) {
  return STAFF_CONFIG.find(s => s.pin === pin) || null;
}

// ── (обратная совместимость) ──────────────────────────────────────────────────
function getStaffByProgram(code) {
  return getStaffBySubject(code);
}
